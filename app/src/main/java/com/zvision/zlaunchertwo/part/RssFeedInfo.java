package com.zvision.zlaunchertwo.part;

public class RssFeedInfo {

    private String rss_status, short_rss_link, full_rss_link;

    public RssFeedInfo() {
    }

    public String getRss_status() {
        return rss_status;
    }

    public void setRss_status(String rss_status) {
        this.rss_status = rss_status;
    }

    public String getShort_rss_link() {
        return short_rss_link;
    }

    public void setShort_rss_link(String short_rss_link) {
        this.short_rss_link = short_rss_link;
    }

    public String getFull_rss_link() {
        return full_rss_link;
    }

    public void setFull_rss_link(String full_rss_link) {
        this.full_rss_link = full_rss_link;
    }
}
